int		match(char *s1, char *s2)
{
	if (s2[0] == '\0')
	{
		if (s1[0] == '\0')
			return (1);
		else
			return (0);
	}
	while (s2[0] == '*' && s2[1] == '*')
		s2++;
	if (s1[0] == '\0' && s2[0] == '*')
		return (match(&s1[0], &s2[1]));
	if (s1[0] == s2[0])
		return (match(&s1[1], &s2[1]));
	if (s1[0] != s2[0])
	{
		if (s2[0] == '*')
		{
			if (match(&s1[1], &s2[0]) + match(&s1[0], &s2[1]))
				return (1);
			else
				return (0);
		}
	}
	return (0);
}
